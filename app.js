openWishlistBtn.onclick = () => {
    wishlistSidebar.style.transform = 'translateX(0)';
};

closeWishlistBtn.onclick = () => {
    wishlistSidebar.style.transform = 'translateX(100%)';
};

function addToWishlist(name, price, img) {
    wishlistContent.innerHTML += `
        <div class="bg-gray-200 p-2 rounded my-2 flex justify-between items-center">
            <div class="flex items-center">
                <img src="${img}" alt="${name}" class="w-16 h-16 object-cover rounded mr-2">
                <div>
                    <h3 class="font-semibold">${name}</h3>
                    <p>${price}</p>
                </div>
            </div>
            <button class="remove-from-wishlist text-pink-900 ml-4" onclick="removeFromWishlist(this)"><i class="fas fa-trash"></i></button>
        </div>
    `;
}

function removeFromWishlist(button) {
    const item = button.closest('div');
    item.remove(); 
}

